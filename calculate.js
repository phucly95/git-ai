/**
 * Adds two numeric values together
 * @param {number} a - The first operand
 * @param {number} b - The second operand
 * @returns {number} The result of a + b
 * @example
 * sum(10, 20); // returns 30
 * sum(1.5, 2.5); // returns 4
 */
function sum(a, b) {
    return a + b;
}

const a = 5;
const b = 3;
console.log(sum(a, b));