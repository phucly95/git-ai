/**
 * Calculate the sum of two numbers
 * @param {number} a - First number
 * @param {number} b - Second number
 * @returns {number} Sum of a and b
 * @example
 * sum(5, 3); // returns 8
 * sum(-2, 7); // returns 5
 */
function sum(a, b) {
    return a + b;
}

const a = 5;
const b = 3;
console.log(sum(a, b));