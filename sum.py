def cong_hai_so(a: float, b: float) -> float:
    """
    Cộng hai số với nhau.
    
    Tham số:
        a: Số thứ nhất
        b: Số thứ hai
    
    Trả về:
        Tổng của a và b
    """
    return a + b # Trả về tổng 2 số human edit


def nhan_hai_so(a: float, b: float) -> float:
    """
    Nhân hai số với nhau.
    
    Tham số:
        a: Số thứ nhất
        b: Số thứ hai
    
    Trả về:
        Tích của a và b
    """
    return a * b # Trả về tích 2 số human edit